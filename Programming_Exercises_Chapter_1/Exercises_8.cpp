/*(Area and perimeter of an equilateral triangle) Write a program that displays the
area and perimeter of an equilateral triangle that has its three sides as 9.2, using the
following formula:
area = 1.732 * (side1)^2/ 4
perimeter = 3 * side1
@uthor Edin Korkic
*/


#include <iostream>

using namespace std;

int main()
{
     cout << " Area of a triangle is " << 1.732 * (9.2 * 9.2) / 4 << endl;
     cout << " Perimeter of a triangle is" << 3 * 9.2 << endl;

    return 0;
}
